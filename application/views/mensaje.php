		<h2>CodeIgniter</h2>
		<img src='<?php echo base_url(); ?> activos/logoci.png'>
		<p>CodeIgniter es un framework PHP para la creación rápida de aplicaciones web. 
		Contiene una serie de librerías que sirven para el desarrollo de aplicaciones web 
		y además propone una manera elegante de desarrollarlas.</p>
		<p>Tiene una manera específica de codificar las páginas web y clasificar sus diferentes 
		scripts, lo cual nos ayuda a que el código esté organizado y sea más fácil de crear y mantener.</p>
		<img src='<?php echo base_url(); ?> activos/compara.png'>
		<p>CodeIgniter implementa el proceso de desarrollo llamado Modelo Vista Controlador (MVC), 
		Model View Controller, que es un estándar en la Programación de Sistemas, 
		utilizado tanto para hacer Aplicaciones Web así como otros sistemas en entornos tradicionales.</p>
